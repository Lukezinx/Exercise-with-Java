public class JustTest {

    private String name;
    private Integer number;
    private String cpf;

    public JustTest(String name, Integer number, String cpf) {
        this.name = name;
        this.number = number;
        this.cpf = cpf;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getNumber() {
        return number;
    }

    public void setNumber(Integer number) {
        this.number = number;
    }

    public String getCpf() {
        return cpf;
    }

}
